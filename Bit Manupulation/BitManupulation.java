public class BitManupulation {
    public static void main(String[] args) {
        // Binary And(&)
        System.out.println(5 & 6);
        // Binary Or(|)
        System.out.println(5|6);
        // Binary Xor(^)
        System.out.println(5^6);
        // Binary One's Complement
        System.out.println(~5);
        // Binary left shift(<<)
        System.out.println(5<<2);
        // Binary right shift(>>)
        System.out.println(6>>1);
    }
}    