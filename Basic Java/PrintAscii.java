public class PrintAscii {
    public static void main(String args[]){
        char ch='a';
        char ch1='A';
        int ascii=(int)ch;
        int ascii1=(int)ch1;
        System.out.println("The ASCII NUMBER OF "+ch+" is "+ascii+".");
        System.out.println("The ASCII NUMBER OF "+ch1+" is "+ascii1+".");
    }
}
