import java.util.*;
public class TypeCasting {
    // Type Casting methods ----->   (Datatype) Data Name
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        float a=25.0f;
        int b=(int)a;     // Convert from float to integer
        System.out.println(b);
        sc.close();
    }    
}
