public class Variables {
    public static void main(String args[]){
        int a =10;        // for integer values use --> int
        int b =20;
        String name ="Sayan";  // for string Use --> String 
        System.out.println(a);
        System.out.println(b);
        System.out.println(name);
        a=50;            // how i change value after declaration
        System.out.println(a);
        name="Sanu";    // how i change value after declaration
        System.out.println(name);
        
    }
}
